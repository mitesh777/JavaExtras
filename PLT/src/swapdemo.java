
public class swapdemo {

	public static void main(String[] args) {
		int num1=5;
		int num2=3;
		int temp=num1;
		System.out.println("Numbers before swapping");
		System.out.println("num1 =" + num1);
		System.out.println("num2 =" + num2);
		num1 = num2;
		num2 = temp;
		System.out.println("Numbers after swapping");
		System.out.println("num1 =" + num1);
		System.out.println("num2 =" + num2);
		num1 = num2;	
		

	}

}
